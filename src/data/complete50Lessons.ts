export const getAllLessons = () => complete50LessonModules.flatMap(module => module.lessons);
export const getLessonById = (lessonId: string) => getAllLessons().find(lesson => lesson.id === lessonId);
export const getModuleById = (moduleId: string) => complete50LessonModules.find(module => module.id === moduleId);
export const getModuleProgress = (moduleId: string, completedLessons: string[]) => {
  const module = getModuleById(moduleId);
  if (!module) return 0;
  const completedInModule = module.lessons.filter(lesson => completedLessons.includes(lesson.id)).length;
  return Math.round((completedInModule / module.lessons.length) * 100);
};